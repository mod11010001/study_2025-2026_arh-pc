;-------------------------------------------------------------------
; Программа вывода сообщения на экран и ввода строки с клавиатуры
;-------------------------------------------------------------------
%include 'in_out.asm'   ; подключение внешнего файла с подпрограммами

SECTION .data
    msg db 'Введите строку: ', 0h   ; сообщение

SECTION .bss
    buf1 resb 80                     ; буфер для ввода строки

SECTION .text
    global _start

_start:
    ; --- Вывод сообщения ---
    mov eax, msg      ; адрес строки в EAX
    call sprint     ; вызов подпрограммы печати строки с переводом строки

    ; --- Ввод строки ---
    mov ecx, buf1     ; адрес буфера для ввода
    mov edx, 80       ; длина буфера
    call sread        ; вызов подпрограммы чтения строки

    ; --- Завершение программы ---
    call quit         ; вызов подпрограммы завершения


